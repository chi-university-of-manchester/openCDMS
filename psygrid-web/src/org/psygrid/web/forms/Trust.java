package org.psygrid.web.forms;

/**
 * An NHS trust belongs to a Hub and together forms a group.
 * 
 * @author Lucy Bridges
 *
 */
public class Trust {

	private String code;
	private String name;
	
	public Trust(String code, String name) {
		this.code = code;
		this.name = name;
	}
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
