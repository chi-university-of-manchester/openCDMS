package org.psygrid.collection.entry.persistence;

public class NoExternalIdMappingException extends Exception {

	public NoExternalIdMappingException() {
		
	}

	public NoExternalIdMappingException(String arg0) {
		super(arg0);
		
	}

	public NoExternalIdMappingException(Throwable arg0) {
		super(arg0);
		
	}

	public NoExternalIdMappingException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

}
